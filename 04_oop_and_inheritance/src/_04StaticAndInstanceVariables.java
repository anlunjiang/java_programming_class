public class _04StaticAndInstanceVariables {
    private static String var;
    // Static variables are also known as static member variables
    // Every instance of that class shares the same static var
    // if changes are made to that var = then all other instances will
    // see the effect of that change

    private String var2;
    // This is an instance variable - opposite of static
    // They belong to only an instance of a class
}
